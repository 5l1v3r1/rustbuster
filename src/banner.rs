pub fn configuration(mode: &str, url: &str, threads: &str, wordlist: &str) -> String {
    format!(
        "[+] Mode\t: {}
[+] Url/Domain\t: {}
[+] Threads\t: {}
[+] Wordlist\t: {}",
        mode, url, threads, wordlist
    )
}

pub fn starting_time() -> String {
    format!(
        "[?] Started at\t: {}\n",
        Local::now().format("%Y-%m-%d %H:%M:%S").to_string()
    )
}

pub fn ending_time() -> String {
    format!(
        "\n[?] Ended at: {}",
        Local::now().format("%Y-%m-%d %H:%M:%S").to_string()
    )
}
